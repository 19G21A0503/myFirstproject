

#String
#Implicit
a = "Mohansai"
print(a)
print(type(a))

#Explicit
a = str("Mohansai")
print(a)
print(type(a))

#Variable Annotation
a: str = "Mohansai"
print(a)
print(type(a))

#Boolean
a = str(True)
print(a)
print(type(a))

#Octal
a = str(0o1234)
print(a)
print(type(a))

#HexaDecimal
a = str(0xface)
print(a)
print(type(a))

#Binary
a = str(0b1010)
print(a)
print(type(a))

#Decimal
#integer
a = str(1234)
print(a)
print(type(a))